# STEP 7: Create Gradio web interface
def create_gradio_interface():
    """Create the Gradio interface"""

    # Store uploaded files
    uploaded_pdfs = []

    def process_uploaded_files(files):
        """Process files uploaded through Gradio"""
        nonlocal uploaded_pdfs
        uploaded_pdfs = files
        if not files:
            return "⚠️ Please upload PDF files first"

        try:
            # Extract text from uploaded files
            pdf_data = []
            for file in files:
                with open(file.name, 'rb') as f:
                    pdf_data.append(f.read())

            raw_text = chatbot.get_pdf_text(pdf_data)
            text_chunks = chatbot.get_text_chunks(raw_text)
            result = chatbot.setup_vector_store(text_chunks)
            return f"✅ {result} Processed {len(text_chunks)} text chunks."

        except Exception as e:
            return f"❌ Error processing files: {str(e)}"

    def chat_response(message, history):
        """Handle chat responses"""
        return chatbot.process_question(message)

    with gr.Blocks(theme=gr.themes.Soft(), title="Company Policies Chatbot") as demo:
        gr.Markdown(
            """
            # 🏢 Company Policies Chatbot

            Upload your company policy PDFs and ask questions!
            """
        )

        with gr.Row():
            with gr.Column(scale=1):
                gr.Markdown("### 📁 Upload Policies")

                file_input = gr.File(
                    label="Upload PDF Files",
                    file_count="multiple",
                    file_types=[".pdf"],
                    height=100
                )

                process_btn = gr.Button("🚀 Process Documents", variant="primary")
                process_status = gr.Textbox(label="Processing Status", interactive=False)

                gr.Markdown("---")
                gr.Markdown("""
                **Example Questions:**
                - What is the vacation policy?
                - How many sick days are allowed?
                - What are the remote work rules?
                - Tell me about the code of conduct
                """)

            with gr.Column(scale=2):
                gr.Markdown("### 💬 Chat Interface")

                chat_interface = gr.ChatInterface(
                    fn=chat_response,
                    title="",
                    description="Ask questions about your company policies...",
                    examples=[
                        "What is the vacation policy?",
                        "How many sick days do employees get?",
                        "What are the remote work requirements?",
                        "Tell me about the IT security policies"
                    ]
                )

        # Event handlers
        process_btn.click(
            fn=process_uploaded_files,
            inputs=file_input,
            outputs=process_status
        )

    return demo

