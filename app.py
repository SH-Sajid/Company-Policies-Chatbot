import gradio as gr
from langchain_community.document_loaders import PyPDFLoader
from langchain.text_splitter import RecursiveCharacterTextSplitter
from langchain_community.embeddings import HuggingFaceEmbeddings
from langchain_community.vectorstores import FAISS
from langchain_groq import ChatGroq
from langchain.chains.question_answering import load_qa_chain
from langchain.prompts import PromptTemplate
import tempfile
import os

class CompanyPoliciesChatbot:
    def __init__(self):
        self.vector_store = None
        self.embeddings = HuggingFaceEmbeddings(
            model_name="sentence-transformers/all-mpnet-base-v2"
        )

    def get_pdf_text(self, pdf_files):
        """Extract text from PDF files"""
        text = ""
        for pdf_file in pdf_files:
            # Save uploaded file temporarily
            with tempfile.NamedTemporaryFile(delete=False, suffix='.pdf') as tmp_file:
                tmp_file.write(pdf_file)
                tmp_file_path = tmp_file.name

            # Load PDF using PyPDFLoader
            loader = PyPDFLoader(tmp_file_path)
            documents = loader.load()

            for doc in documents:
                text += doc.page_content + "\n"

            # Clean up temporary file
            os.unlink(tmp_file_path)

        return text

    def get_text_chunks(self, text):
        """Split text into chunks"""
        text_splitter = RecursiveCharacterTextSplitter(
            chunk_size=1000,
            chunk_overlap=200
        )
        chunks = text_splitter.split_text(text)
        return chunks

    def setup_vector_store(self, text_chunks):
        """Create vector store"""
        self.vector_store = FAISS.from_texts(text_chunks, embedding=self.embeddings)
        return "✅ Documents processed successfully! You can now ask questions."

    def get_conversational_chain(self):
        """Create the QA chain with Groq"""
        prompt_template = """
        You are a helpful assistant that answers questions about company policies based on the provided context.

        Context: {context}

        Question: {question}

        Please provide a detailed answer based only on the context above.
        If the answer cannot be found in the context, say "I cannot find this information in the provided documents."
        Do not make up information.

        Answer:
        """

        model = ChatGroq(
            model="llama-3.1-8b-instant",
            temperature=0.1,
            groq_api_key=os.environ['GROQ_API_KEY']
        )

        prompt = PromptTemplate(
            template=prompt_template,
            input_variables=["context", "question"]
        )
        chain = load_qa_chain(model, chain_type="stuff", prompt=prompt)
        return chain

    def process_question(self, user_question):
        """Process user question and return response"""
        if not self.vector_store:
            return "❌ Please process documents first by uploading PDFs and clicking 'Process Documents'"

        try:
            # Search for relevant documents
            docs = self.vector_store.similarity_search(user_question, k=3)

            # Get response from chain
            chain = self.get_conversational_chain()
            response = chain(
                {"input_documents": docs, "question": user_question},
                return_only_outputs=True
            )

            # Format response with sources
            sources_text = "\n\n📚 **Sources:**\n"
            for i, doc in enumerate(docs, 1):
                source_name = "Uploaded Document"
                content_preview = doc.page_content[:150] + "..." if len(doc.page_content) > 150 else doc.page_content
                sources_text += f"{i}. {content_preview}\n"

            return f"🤖 {response['output_text']}{sources_text}"

        except Exception as e:
            return f"❌ Error: {str(e)}"

